int	ft_sqrt(int nb)
{
	int	sqrt;
	int	odd_nb;

	sqrt = 0;
	odd_nb = 1;
	while (nb > 0)
	{
		nb -= odd_nb;
		sqrt ++;
		odd_nb += 2;
	}
	return (sqrt);
}
